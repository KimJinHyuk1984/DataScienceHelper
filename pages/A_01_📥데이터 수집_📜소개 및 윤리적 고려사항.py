import streamlit as st

st.header("1. 데이터 수집 소개 및 윤리적 고려사항")

st.subheader("1.1 데이터 수집이란?")
st.markdown("""
데이터 수집은 분석, 시각화, 모델링 등 후속 작업을 위해 필요한 데이터를 다양한 출처로부터 얻는 과정을 의미합니다.
데이터의 품질과 양은 분석 결과의 질을 결정하는 매우 중요한 요소입니다.

**주요 데이터 출처:**
-   **파일:** CSV, Excel, JSON, XML, TXT 등 로컬 또는 원격 저장소의 파일.
-   **데이터베이스:** SQL(관계형) 데이터베이스 (MySQL, PostgreSQL, SQLite 등), NoSQL 데이터베이스 (MongoDB 등).
-   **웹 API (Application Programming Interface):** 특정 서비스 제공자가 외부에서 데이터를 가져갈 수 있도록 규칙에 따라 공개한 인터페이스. (예: 공공 데이터 포털, 소셜 미디어 API)
-   **웹 페이지 (Web Scraping/Crawling):** 웹사이트의 HTML 내용을 직접 파싱하여 필요한 정보를 추출하는 방식.
-   **센서 및 IoT 장치:** 실시간으로 생성되는 스트리밍 데이터.
-   **설문 조사 및 실험:** 직접 데이터를 생성하고 수집.
""")

st.markdown("---")

st.subheader("1.2 데이터 수집 시 윤리적 및 법적 고려사항")
st.markdown("""
**데이터를 수집하고 활용할 때는 반드시 윤리적, 법적 문제를 신중하게 고려해야 합니다.**
무분별한 데이터 수집은 법적 제재를 받거나 서비스 제공자로부터 차단될 수 있으며, 개인의 프라이버시를 침해할 수 있습니다.
""")

st.error("""
**🚨 중요: 다음 사항들을 반드시 숙지하고 준수하세요!**

1.  **개인정보보호:**
    -   개인정보(이름, 주민등록번호, 연락처, 주소, 이메일, 생체 정보 등) 수집 시에는 반드시 정보 주체의 명시적인 동의를 받아야 하며, 관련 법규(예: 개인정보보호법)를 준수해야 합니다.
    -   수집한 개인정보는 안전하게 관리하고, 목적 달성 후에는 파기해야 합니다.
    -   익명화/가명화 처리를 통해 개인 식별 가능성을 최소화하는 것이 좋습니다.

2.  **저작권 (Copyright):**
    -   수집하려는 데이터(텍스트, 이미지, 영상 등)에 저작권이 있는지 확인해야 합니다.
    -   저작권이 있는 데이터를 무단으로 수집, 복제, 배포하는 것은 법적 문제를 야기할 수 있습니다.
    -   공개된 데이터라도 출처를 명시하고, 원저작자의 권리를 존중해야 합니다.

3.  **서비스 이용 약관 (Terms of Service, ToS):**
    -   웹사이트나 API 서비스는 대부분 자체적인 이용 약관을 가지고 있습니다. 데이터를 수집하기 전에 반드시 해당 약관을 확인하고, 명시된 데이터 수집 및 활용 범위를 준수해야 합니다.
    -   약관에서 금지하는 방식으로 데이터를 수집하거나 활용해서는 안 됩니다.

4.  **`robots.txt` (웹 스크레이핑 시):**
    -   `robots.txt` 파일은 웹사이트 루트 디렉토리(예: `https://example.com/robots.txt`)에 위치하며, 웹 로봇(크롤러)이 사이트의 어떤 부분에 접근할 수 있고 없는지를 명시합니다.
    -   웹 스크레이핑 시에는 반드시 `robots.txt` 파일을 확인하고, 접근이 허용된 페이지만 수집 대상으로 해야 합니다. `Disallow` 지시어를 존중해야 합니다.

5.  **서버 부하 및 요청 빈도 (Rate Limiting):**
    -   웹 API나 웹사이트에 너무 잦은 요청을 보내면 해당 서버에 과도한 부하를 줄 수 있습니다. 이는 서비스 장애를 유발하거나 악의적인 공격으로 간주되어 IP가 차단될 수 있습니다.
    -   API의 경우, 제공자가 명시한 요청 제한(rate limit)을 반드시 준수해야 합니다.
    -   웹 스크레이핑 시에는 요청 사이에 적절한 시간 간격(`time.sleep()`)을 두어 서버에 부담을 주지 않도록 해야 합니다.

6.  **데이터의 정확성 및 편향성:**
    -   수집된 데이터가 정확한지, 특정 그룹에 편향되어 있지는 않은지 비판적으로 검토해야 합니다.
    -   편향된 데이터로 학습된 모델은 공정하지 못한 결과를 초래할 수 있습니다.

7.  **투명성 및 책임감:**
    -   데이터 수집 목적과 활용 방안을 명확히 하고, 가능한 경우 이를 공개하는 것이 좋습니다.
    -   데이터 수집 및 활용 과정에서 발생하는 문제에 대해 책임감을 가져야 합니다.
""")

